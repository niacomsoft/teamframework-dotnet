namespace Niacomsoft
{
    /// <summary> 提供了 <see langword="true" /> 值相关的常量。 </summary>
    public static class TrueValue
    {
        /// <summary> <see langword="true" /> 值。 </summary>
        public const bool Value = true;

        /// <summary> <see langword="true" /> 等效的 <see cref="int" /> 类型的值。 </summary>
        public const int IntValue = 1;

        /// <summary> <see langword="true" /> 等效的 <see cref="byte" /> 类型的值。 </summary>
        public const byte ByteValue = 1;

        /// <summary> <see langword="true" /> 等效的 <see cref="char" /> 类型的值（ <c> Y </c>）。 </summary>
        public const char Y = 'Y';

        /// <summary> <see langword="true" /> 等效的 <see cref="char" /> 类型的值（ <c> T </c>）。 </summary>
        public const char T = 'T';

        /// <summary> <see langword="true" /> 等效的字符串。 </summary>
        public const string TrueString = "True";
    }
}